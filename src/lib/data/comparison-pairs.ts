export function pairSlug(a: string, b: string) {
  return `${a}-vs-${b}`;
}

export function parsePairSlug(slug: string): [string, string] | null {
  const idx = slug.lastIndexOf("-vs-");
  if (idx <= 0) return null;
  const a = slug.slice(0, idx);
  const b = slug.slice(idx + 4);
  if (!a || !b) return null;
  return [a, b];
}
